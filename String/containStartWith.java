package String;
import java.util.*;
public class containStartWith {
    public static void main(String[] args) {
        String str = "kuch bhi print kar do ";
        System.out.println(str);
        System.out.println();

        System.out.println(str.contains(" bhi"));
        System.out.println(str.startsWith("uch"));
        System.out.println(str.endsWith("do "));
    }
}
